#include "bsp_relay.h"

namespace bsp {

Relay::Relay(GPIO_TypeDef* relay_group, uint16_t relay_pin): relay_(relay_group, relay_pin) {}

void Relay::On(){
  relay_.High();
}

void Relay::Off(){
  relay_.Low();
}

} /* namespace bsp */
